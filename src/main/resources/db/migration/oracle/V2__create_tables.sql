    ADD CONSTRAINT fk_cf_movie_list_user
        FOREIGN KEY (user_id) REFERENCES cf_user (id);

ALTER TABLE cf_review
    ADD CONSTRAINT fk_cf_review_movie
        FOREIGN KEY (movie_id) REFERENCES cf_movie (id);

ALTER TABLE cf_review
    ADD CONSTRAINT fk_cf_review_user
        FOREIGN KEY (user_id) REFERENCES cf_user (id);

ALTER TABLE cf_movie_genres
    ADD CONSTRAINT fk_cf_movie_genres_movie
        FOREIGN KEY (movie_id) REFERENCES cf_movie (id);

ALTER TABLE cf_movie_list_movies
    ADD CONSTRAINT fk_cf_movie_list_movies_movie
        FOREIGN KEY (movie_id) REFERENCES cf_movie (id);

ALTER TABLE cf_movie_list_movies
    ADD CONSTRAINT fk_cf_movie_list_movies_list
        FOREIGN KEY (movie_list_id) REFERENCES cf_movie_list (id);