CREATE TABLE cf_movie
(
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    title          VARCHAR(255),
    description    VARCHAR(255),
    release_date   date,
    average_rating DECIMAL,
    CONSTRAINT pk_cf_movie PRIMARY KEY (id)
);

CREATE TABLE cf_movie_genres
(
    movie_id BIGINT NOT NULL,
    genre    VARCHAR(255)
);

CREATE TABLE cf_movie_list
(
    id      BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name    VARCHAR(255),
    user_id BIGINT,
    CONSTRAINT pk_cf_movie_list PRIMARY KEY (id)
);

CREATE TABLE cf_movie_list_movies
(
    movie_id      BIGINT NOT NULL,
    movie_list_id BIGINT NOT NULL
);

CREATE TABLE cf_review
(
    id       BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    user_id  BIGINT,
    movie_id BIGINT,
    rating   DECIMAL,
    comments VARCHAR(255),
    CONSTRAINT pk_cf_review PRIMARY KEY (id)
);

CREATE TABLE cf_user
(
    id       BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name     VARCHAR(255),
    email    VARCHAR(255),
    password VARCHAR(255),
    age      INT,
    is_admin BOOLEAN,
    CONSTRAINT pk_cf_user PRIMARY KEY (id)
);

