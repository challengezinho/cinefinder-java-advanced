    ADD CONSTRAINT FK_CF_REVIEW_ON_MOVIE FOREIGN KEY (movie_id) REFERENCES cf_movie (id);

ALTER TABLE cf_review
    ADD CONSTRAINT FK_CF_REVIEW_ON_USER FOREIGN KEY (user_id) REFERENCES cf_user (id);

ALTER TABLE cf_movie_genres
    ADD CONSTRAINT fk_cf_movie_genres_on_movie FOREIGN KEY (movie_id) REFERENCES cf_movie (id);

